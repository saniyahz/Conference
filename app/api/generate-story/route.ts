import { NextRequest, NextResponse } from 'next/server'
import { GoogleGenerativeAI } from '@google/generative-ai'

const genAI = new GoogleGenerativeAI(process.env.GOOGLE_API_KEY || process.env.GEMINI_API_KEY || '')

export async function POST(request: NextRequest) {
  try {
    const { prompt } = await request.json()

    if (!prompt || typeof prompt !== 'string') {
      return NextResponse.json(
        { error: 'Invalid prompt provided' },
        { status: 400 }
      )
    }

    console.log('📖 Generating unique story with Google Gemini from:', prompt)

    // Use Gemini to generate a unique, kid-friendly story
    const model = genAI.getGenerativeModel({ model: 'gemini-1.5-flash' })

    const systemPrompt = `You are a creative children's story writer creating stories for the Google Gemini Storybook format. Create engaging, unique stories for kids aged 4-8 based on their ideas.

IMPORTANT RULES:
- Create a UNIQUE story based on what the child says - don't use generic templates
- The story should be 10-12 pages long (like Gemini Storybook format)
- Each page should be 2-3 sentences - short and simple for kids
- Include a clear beginning, middle, and end
- Include a simple problem and how it's solved
- Keep it kid-friendly, non-religious, non-political
- Make it fun, magical, and age-appropriate
- Use the EXACT character names and details the child mentions
- Each page should describe a moment that can be illustrated

FORMAT YOUR RESPONSE EXACTLY LIKE THIS:
TITLE: [Creative title based on their idea]

PAGE 1:
[2-3 sentences - introduction]

PAGE 2:
[2-3 sentences - setting the scene]

PAGE 3:
[2-3 sentences - character's world]

PAGE 4:
[2-3 sentences - adventure begins]

PAGE 5:
[2-3 sentences - discovering the problem]

PAGE 6:
[2-3 sentences - facing the challenge]

PAGE 7:
[2-3 sentences - making a plan]

PAGE 8:
[2-3 sentences - working together]

PAGE 9:
[2-3 sentences - solving the problem]

PAGE 10:
[2-3 sentences - celebrating success]

PAGE 11:
[2-3 sentences - what they learned]

PAGE 12:
[2-3 sentences - happy ending]`

    const fullPrompt = `${systemPrompt}

A child wants a story about: "${prompt}".

Create a unique, magical 12-page story based on their idea! Make it fun, engaging, and perfect for kids.`

    const result = await model.generateContent(fullPrompt)
    const response = await result.response
    const storyText = response.text()

    console.log('✅ Story generated by Google Gemini')

    // Parse the story
    const story = parseStory(storyText, prompt)

    // Generate image prompts for each page
    const imagePrompts = generateImagePrompts(story, prompt)

    return NextResponse.json({
      story: {
        title: story.title,
        pages: story.pages,
      },
      imagePrompts,
    })
  } catch (error) {
    console.error('Error generating story:', error)
    return NextResponse.json(
      { error: 'Failed to generate story. Please try again.' },
      { status: 500 }
    )
  }
}

function generateImagePrompts(story: any, originalPrompt: string): string[] {
  // Simple art style for consistent, kid-friendly images
  const artStyle = "Children's book illustration, colorful, warm, whimsical, magical, detailed"

  // Extract character details from the original prompt
  const lowerPrompt = originalPrompt.toLowerCase()

  // Extract specific character description (like "dog with a horn", "cat named Luna", etc.)
  let characterDescription = ''

  // Try to extract full character description
  if (lowerPrompt.includes('dog')) {
    // Get everything after "dog" until we hit common words
    const dogMatch = originalPrompt.match(/dog[^.!?,]*/i)
    characterDescription = dogMatch ? dogMatch[0] : 'dog'
  } else if (lowerPrompt.includes('cat')) {
    const catMatch = originalPrompt.match(/cat[^.!?,]*/i)
    characterDescription = catMatch ? catMatch[0] : 'cat'
  } else if (lowerPrompt.includes('dragon')) {
    const dragonMatch = originalPrompt.match(/dragon[^.!?,]*/i)
    characterDescription = dragonMatch ? dragonMatch[0] : 'dragon'
  } else if (lowerPrompt.includes('unicorn')) {
    characterDescription = 'unicorn'
  } else {
    // Try to extract any character description
    const characterMatch = originalPrompt.match(/(?:a|an|the)\s+([^.!?,]+?)(?:\s+who|\s+that|goes|went|$)/i)
    characterDescription = characterMatch ? characterMatch[1] : 'friendly character'
  }

  console.log('🎨 Character description extracted:', characterDescription)

  // Generate prompts for 12 pages - each based on the story content
  const prompts = story.pages.map((page: any, index: number) => {
    // Extract key elements from the page text
    const pageText = page.text.toLowerCase()

    // Build a prompt that combines the character and the page content
    let sceneDescription = ''

    // Try to infer the scene from the text
    if (index === 0) {
      sceneDescription = `${characterDescription} with bright expressive eyes in a cozy beautiful home with warm lighting`
    } else if (index === 1) {
      sceneDescription = `${characterDescription} in a magical land with sparkling nature and beautiful scenery`
    } else if (pageText.includes('forest') || pageText.includes('tree')) {
      sceneDescription = `${characterDescription} in an enchanted forest with mystical trees and magical atmosphere`
    } else if (pageText.includes('friend') || pageText.includes('together')) {
      sceneDescription = `${characterDescription} with friendly animal companions in a beautiful setting`
    } else if (pageText.includes('problem') || pageText.includes('sad') || pageText.includes('worry')) {
      sceneDescription = `${characterDescription} looking concerned but determined, dramatic lighting with storm or gray clouds`
    } else if (pageText.includes('idea') || pageText.includes('plan') || pageText.includes('think')) {
      sceneDescription = `${characterDescription} thinking and planning with friends in a magical clearing`
    } else if (pageText.includes('work') || pageText.includes('help') || pageText.includes('together')) {
      sceneDescription = `${characterDescription} and friends working together cooperatively with magical sparkles`
    } else if (pageText.includes('happy') || pageText.includes('joy') || pageText.includes('celebrate')) {
      sceneDescription = `${characterDescription} celebrating with friends in a beautiful meadow, everyone happy and joyful`
    } else if (pageText.includes('learn') || pageText.includes('discover')) {
      sceneDescription = `${characterDescription} with friends sharing a peaceful moment at sunset`
    } else if (pageText.includes('end') || index === story.pages.length - 1) {
      sceneDescription = `${characterDescription} happy and content at home with stars and moonlight, perfect peaceful ending`
    } else {
      sceneDescription = `${characterDescription} on an adventure in a magical land`
    }

    return `${artStyle}. ${sceneDescription}. Make sure the ${characterDescription} is the main focus of the image and clearly visible.`
  })

  return prompts
}

function parseStory(text: string, originalPrompt: string) {
  // Extract title
  const titleMatch = text.match(/TITLE:\s*(.+?)(?:\n|PAGE)/i)
  const title = titleMatch ? titleMatch[1].trim() : 'My Amazing Story'

  // Extract pages (looking for 12 pages)
  const pages: { text: string }[] = []

  for (let i = 1; i <= 12; i++) {
    const pageRegex = new RegExp(`PAGE ${i}:\\s*(.+?)(?=PAGE ${i + 1}:|$)`, 'is')
    const pageMatch = text.match(pageRegex)

    if (pageMatch) {
      const pageText = pageMatch[1].trim()
      // Clean up any extra whitespace
      const cleanedText = pageText.replace(/\n\n+/g, '\n').trim()
      pages.push({ text: cleanedText })
    }
  }

  // Fallback if parsing fails - split into 12 pages
  if (pages.length < 10) {
    const sentences = text.split(/[.!?]+/).filter(s => s.trim().length > 20)
    const sentencesPerPage = Math.max(2, Math.floor(sentences.length / 12))

    pages.length = 0 // Clear any partial pages
    for (let i = 0; i < 12; i++) {
      const start = i * sentencesPerPage
      const end = start + sentencesPerPage
      const pageText = sentences.slice(start, end).join('. ') + '.'
      if (pageText.trim().length > 5) {
        pages.push({ text: pageText.trim() })
      }
    }
  }

  // Final fallback - create 12 simple pages
  if (pages.length < 10) {
    const char = extractCharacterName(originalPrompt)
    pages.length = 0
    pages.push(
      { text: `Once upon a time, there was a wonderful ${char}.` },
      { text: `${char} lived in a magical land full of wonder and beauty.` },
      { text: `Every day was an adventure waiting to happen!` },
      { text: `One day, ${char} decided to go on a special quest.` },
      { text: `Along the way, ${char} discovered something amazing.` },
      { text: `But there was a problem that needed to be solved!` },
      { text: `${char} thought carefully about what to do.` },
      { text: `With help from friends, they worked together as a team.` },
      { text: `Through teamwork and courage, they solved the problem!` },
      { text: `Everyone celebrated their wonderful success.` },
      { text: `${char} learned that friendship makes everything better.` },
      { text: `And they all lived happily ever after! The End.` }
    )
  }

  // Ensure exactly 12 pages
  while (pages.length < 12) {
    pages.push({ text: 'The adventure continued with more magical moments...' })
  }

  return {
    title,
    pages: pages.slice(0, 12),
  }
}

function extractCharacterName(prompt: string): string {
  const lowerPrompt = prompt.toLowerCase()

  // Try to find a character type
  const animals = ['dog', 'cat', 'dragon', 'unicorn', 'bear', 'lion', 'elephant', 'monkey', 'rabbit', 'fox', 'dinosaur', 'bird']
  for (const animal of animals) {
    if (lowerPrompt.includes(animal)) {
      return animal
    }
  }

  const people = ['princess', 'prince', 'knight', 'wizard', 'fairy', 'pirate']
  for (const person of people) {
    if (lowerPrompt.includes(person)) {
      return person
    }
  }

  return 'our hero'
}
