import { NextRequest, NextResponse } from 'next/server'
import OpenAI from 'openai'

const openai = new OpenAI({
  apiKey: process.env.OPENAI_API_KEY,
})

export async function POST(request: NextRequest) {
  try {
    const { prompt } = await request.json()

    if (!prompt || typeof prompt !== 'string') {
      return NextResponse.json(
        { error: 'Invalid prompt provided' },
        { status: 400 }
      )
    }

    console.log('📖 Generating unique story from:', prompt)

    // Use OpenAI to generate a unique, kid-friendly story based on what they said
    const completion = await openai.chat.completions.create({
      model: 'gpt-4',
      messages: [
        {
          role: 'system',
          content: `You are a creative children's story writer. Create engaging, unique stories for kids aged 4-8 based on their ideas.

IMPORTANT RULES:
- Create a UNIQUE story based on what the child says - don't use generic templates
- The story should have a clear beginning, middle, and end
- Include a simple problem and how it's solved
- Keep it kid-friendly, non-religious, non-political
- Make it fun, magical, and age-appropriate
- The story should be 4 paragraphs (one per page)
- Each paragraph should be 3-4 sentences
- Use the EXACT character names and details the child mentions

FORMAT YOUR RESPONSE EXACTLY LIKE THIS:
TITLE: [Creative title based on their idea]

PAGE 1:
[First paragraph - introduction of character and setting]

PAGE 2:
[Second paragraph - the adventure begins, introduce the problem]

PAGE 3:
[Third paragraph - working to solve the problem]

PAGE 4:
[Fourth paragraph - problem solved, happy ending]`
        },
        {
          role: 'user',
          content: `A child wants a story about: "${prompt}". Create a unique, magical story based on their idea!`
        }
      ],
      temperature: 0.9, // High creativity
      max_tokens: 1000,
    })

    const storyText = completion.choices[0].message.content || ''
    console.log('✅ Story generated by OpenAI')

    // Parse the story
    const story = parseStory(storyText, prompt)

    // Generate image prompts based on the actual story content
    const imagePrompts = generateImagePrompts(story, prompt)

    return NextResponse.json({
      story: {
        title: story.title,
        pages: story.pages,
      },
      imagePrompts,
    })
  } catch (error) {
    console.error('Error generating story:', error)
    return NextResponse.json(
      { error: 'Failed to generate story. Please try again.' },
      { status: 500 }
    )
  }
}

function generateImagePrompts(story: any, originalPrompt: string): string[] {
  // Simple art style for consistent, kid-friendly images
  const artStyle = "Children's book illustration, colorful, warm, whimsical, magical"

  // Extract any character mentions from the prompt
  const lowerPrompt = originalPrompt.toLowerCase()
  let character = 'friendly character'

  // Try to extract character type
  const animals = ['dog', 'cat', 'dragon', 'unicorn', 'bear', 'lion', 'elephant', 'monkey', 'rabbit', 'fox', 'dinosaur', 'bird']
  for (const animal of animals) {
    if (lowerPrompt.includes(animal)) {
      character = animal
      break
    }
  }

  // Create concise prompts for each page with character AND scenery
  const prompts = [
    // Page 1: Introduction scene
    `${artStyle}. A cute ${character} with bright eyes in a magical land with sparkling trees, colorful flowers, and butterflies at sunrise.`,

    // Page 2: Adventure begins
    `${artStyle}. The ${character} on an adventure in an enchanted forest with mystical trees, rainbow flowers, and friendly animals.`,

    // Page 3: Solving the problem together
    `${artStyle}. The ${character} with animal friends working together in a forest clearing with magical sparkles and a glowing rainbow in the sky.`,

    // Page 4: Happy ending celebration
    `${artStyle}. The ${character} celebrating with friends in a meadow at sunset with orange, pink, and gold sky, magical sparkles everywhere.`,
  ]

  return prompts
}

function parseStory(text: string, originalPrompt: string) {
  // Extract title
  const titleMatch = text.match(/TITLE:\s*(.+?)(?:\n|PAGE)/i)
  const title = titleMatch ? titleMatch[1].trim() : 'My Amazing Story'

  // Extract pages
  const pages: { text: string }[] = []

  for (let i = 1; i <= 4; i++) {
    const pageRegex = new RegExp(`PAGE ${i}:\\s*(.+?)(?=PAGE ${i + 1}:|$)`, 'is')
    const pageMatch = text.match(pageRegex)

    if (pageMatch) {
      pages.push({ text: pageMatch[1].trim() })
    }
  }

  // Fallback if parsing fails
  if (pages.length === 0) {
    const sentences = text.split(/[.!?]+/).filter(s => s.trim().length > 0)
    const sentencesPerPage = Math.ceil(sentences.length / 4)

    for (let i = 0; i < 4; i++) {
      const start = i * sentencesPerPage
      const end = start + sentencesPerPage
      const pageText = sentences.slice(start, end).join('. ') + '.'
      if (pageText.trim().length > 1) {
        pages.push({ text: pageText.trim() })
      }
    }
  }

  // Final fallback
  if (pages.length === 0) {
    pages.push(
      { text: `Once upon a time, there was an adventure about ${originalPrompt}.` },
      { text: 'Amazing things started to happen, and everyone was excited!' },
      { text: 'The journey was full of surprises and wonderful moments.' },
      { text: 'And they all lived happily ever after. The End!' }
    )
  }

  // Ensure we have exactly 4 pages
  while (pages.length < 4) {
    pages.push({ text: 'And the adventure continued...' })
  }

  return {
    title,
    pages: pages.slice(0, 4),
  }
}
