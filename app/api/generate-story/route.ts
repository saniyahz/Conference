import { NextRequest, NextResponse } from 'next/server'
import OpenAI from 'openai'

const openai = new OpenAI({
  apiKey: process.env.OPENAI_API_KEY,
})

export async function POST(request: NextRequest) {
  try {
    const { prompt } = await request.json()

    if (!prompt || typeof prompt !== 'string') {
      return NextResponse.json(
        { error: 'Invalid prompt provided' },
        { status: 400 }
      )
    }

    console.log('📖 Generating unique 10-page story with OpenAI from:', prompt)

    // Use OpenAI GPT-4 to generate a unique, kid-friendly 10-page story (like Gemini Storybook)
    const completion = await openai.chat.completions.create({
      model: 'gpt-4',
      messages: [
        {
          role: 'system',
          content: `You are a creative children's story writer. Create engaging, unique 10-page stories for kids aged 4-8 based on their ideas (like Google Gemini Storybook format).

IMPORTANT RULES:
- Create a UNIQUE story based on what the child says - don't use generic templates
- The story should be EXACTLY 10 pages long
- Each page should be 2-3 sentences - short and simple for kids
- Include a clear beginning, middle, and end
- Include a simple problem and how it's solved
- Keep it kid-friendly, non-religious, non-political
- Make it fun, magical, and age-appropriate
- Use the EXACT character names and details the child mentions
- Each page should describe a clear visual scene that can be illustrated

FORMAT YOUR RESPONSE EXACTLY LIKE THIS:
TITLE: [Creative title based on their idea]

PAGE 1:
[2-3 sentences - introduction of main character]

PAGE 2:
[2-3 sentences - character's normal world]

PAGE 3:
[2-3 sentences - something changes, adventure begins]

PAGE 4:
[2-3 sentences - discovering the problem]

PAGE 5:
[2-3 sentences - facing the challenge]

PAGE 6:
[2-3 sentences - trying to solve it]

PAGE 7:
[2-3 sentences - working together/making progress]

PAGE 8:
[2-3 sentences - solving the problem successfully]

PAGE 9:
[2-3 sentences - celebrating and what they learned]

PAGE 10:
[2-3 sentences - happy ending, back home]`
        },
        {
          role: 'user',
          content: `A child wants a story about: "${prompt}". Create a unique, magical 10-page story based on their idea!`
        }
      ],
      temperature: 0.9,
      max_tokens: 1500,
    })

    const storyText = completion.choices[0].message.content || ''
    console.log('✅ 10-page story generated by OpenAI GPT-4')

    // Parse the story
    const story = parseStory(storyText, prompt)

    // Generate image prompts for each page
    const imagePrompts = generateImagePrompts(story, prompt)

    return NextResponse.json({
      story: {
        title: story.title,
        pages: story.pages,
      },
      imagePrompts,
    })
  } catch (error) {
    console.error('Error generating story:', error)
    return NextResponse.json(
      { error: 'Failed to generate story. Please try again.' },
      { status: 500 }
    )
  }
}

function generateImagePrompts(story: any, originalPrompt: string): string[] {
  // Professional art style like Gemini Storybook
  const artStyle = "Professional children's book illustration, highly detailed, beautiful soft lighting, rich colors, painterly style, storybook quality, magical atmosphere"

  // Extract character details from the original prompt
  const lowerPrompt = originalPrompt.toLowerCase()

  // Extract specific character description
  let characterDescription = ''

  // Try to extract full character description - checking for common animals
  if (lowerPrompt.includes('lion')) {
    const lionMatch = originalPrompt.match(/lion[^.!?,]*/i)
    characterDescription = lionMatch ? lionMatch[0] : 'lion'
  } else if (lowerPrompt.includes('dog')) {
    const dogMatch = originalPrompt.match(/dog[^.!?,]*/i)
    characterDescription = dogMatch ? dogMatch[0] : 'dog'
  } else if (lowerPrompt.includes('cat')) {
    const catMatch = originalPrompt.match(/cat[^.!?,]*/i)
    characterDescription = catMatch ? catMatch[0] : 'cat'
  } else if (lowerPrompt.includes('dragon')) {
    const dragonMatch = originalPrompt.match(/dragon[^.!?,]*/i)
    characterDescription = dragonMatch ? dragonMatch[0] : 'dragon'
  } else if (lowerPrompt.includes('unicorn')) {
    characterDescription = 'unicorn'
  } else {
    // Try to extract any character description
    const characterMatch = originalPrompt.match(/(?:a|an|the)\s+([^.!?,]+?)(?:\s+who|\s+that|goes|went|$)/i)
    characterDescription = characterMatch ? characterMatch[1] : 'friendly character'
  }

  console.log('🎨 Character description extracted:', characterDescription)

  // Generate detailed prompts for 10 pages - matching Gemini quality
  const prompts = story.pages.map((page: any, index: number) => {
    const pageText = page.text.toLowerCase()
    let sceneDescription = ''

    // Create scene based on page content and index
    if (index === 0) {
      sceneDescription = `A ${characterDescription} with bright expressive eyes, friendly face, in a beautiful natural setting with detailed background, warm golden lighting, highly detailed`
    } else if (index === 1) {
      sceneDescription = `${characterDescription} in their home environment, cozy atmosphere, detailed surroundings, warm natural lighting, peaceful scene`
    } else if (index === 2) {
      sceneDescription = `${characterDescription} looking excited, starting a journey, beautiful landscape with path ahead, dynamic composition, inspiring`
    } else if (index === 3) {
      sceneDescription = `${characterDescription} discovering something unusual, dramatic lighting, detailed expressive face, atmospheric`
    } else if (index === 4) {
      sceneDescription = `${characterDescription} facing a challenge with determination, brave pose, detailed environment, hopeful lighting`
    } else if (index === 5) {
      sceneDescription = `${characterDescription} actively solving the problem, action pose, detailed scene, dynamic`
    } else if (index === 6) {
      sceneDescription = `${characterDescription} with animal friends cooperating, teamwork scene, detailed characters interacting, warm friendship lighting`
    } else if (index === 7) {
      sceneDescription = `${characterDescription} succeeding, triumphant, bright magical lighting with sparkles, joyful atmosphere, celebration`
    } else if (index === 8) {
      sceneDescription = `${characterDescription} happy celebrating with friends, beautiful sunset, warm orange and gold lighting, peaceful joy`
    } else {
      sceneDescription = `${characterDescription} content at home, peaceful ending, twilight sky with stars, perfect storybook ending`
    }

    return `${artStyle}. ${sceneDescription}. The ${characterDescription} should be the main subject, highly detailed and expressive.`
  })

  return prompts
}

function parseStory(text: string, originalPrompt: string) {
  // Extract title
  const titleMatch = text.match(/TITLE:\s*(.+?)(?:\n|PAGE)/i)
  const title = titleMatch ? titleMatch[1].trim() : 'My Amazing Story'

  // Extract pages (looking for 10 pages)
  const pages: { text: string }[] = []

  for (let i = 1; i <= 10; i++) {
    const pageRegex = new RegExp(`PAGE ${i}:\\s*(.+?)(?=PAGE ${i + 1}:|$)`, 'is')
    const pageMatch = text.match(pageRegex)

    if (pageMatch) {
      const pageText = pageMatch[1].trim()
      // Clean up any extra whitespace
      const cleanedText = pageText.replace(/\n\n+/g, '\n').trim()
      pages.push({ text: cleanedText })
    }
  }

  // Fallback if parsing fails - split into 10 pages
  if (pages.length < 8) {
    const sentences = text.split(/[.!?]+/).filter(s => s.trim().length > 20)
    const sentencesPerPage = Math.max(2, Math.floor(sentences.length / 10))

    pages.length = 0 // Clear any partial pages
    for (let i = 0; i < 10; i++) {
      const start = i * sentencesPerPage
      const end = start + sentencesPerPage
      const pageText = sentences.slice(start, end).join('. ') + '.'
      if (pageText.trim().length > 5) {
        pages.push({ text: pageText.trim() })
      }
    }
  }

  // Final fallback - create 10 simple pages
  if (pages.length < 8) {
    const char = extractCharacterName(originalPrompt)
    pages.length = 0
    pages.push(
      { text: `Once upon a time, there was a wonderful ${char}.` },
      { text: `${char} lived in a magical land full of wonder and beauty.` },
      { text: `One day, ${char} decided to go on a special adventure.` },
      { text: `Along the way, ${char} discovered something amazing.` },
      { text: `But there was a problem that needed to be solved!` },
      { text: `${char} thought carefully and made a brave plan.` },
      { text: `With help from friends, they worked together as a team.` },
      { text: `Through teamwork and courage, they solved the problem!` },
      { text: `Everyone celebrated their wonderful success. ${char} learned that friendship makes everything better.` },
      { text: `And they all lived happily ever after! The End.` }
    )
  }

  // Ensure exactly 10 pages
  while (pages.length < 10) {
    pages.push({ text: 'The adventure continued with more magical moments...' })
  }

  return {
    title,
    pages: pages.slice(0, 10),
  }
}

function extractCharacterName(prompt: string): string {
  const lowerPrompt = prompt.toLowerCase()

  // Try to find a character type
  const animals = ['dog', 'cat', 'dragon', 'unicorn', 'bear', 'lion', 'elephant', 'monkey', 'rabbit', 'fox', 'dinosaur', 'bird']
  for (const animal of animals) {
    if (lowerPrompt.includes(animal)) {
      return animal
    }
  }

  const people = ['princess', 'prince', 'knight', 'wizard', 'fairy', 'pirate']
  for (const person of people) {
    if (lowerPrompt.includes(person)) {
      return person
    }
  }

  return 'our hero'
}
